# Открываем файл с данными
with open('data.txt', 'r') as f:
    # Создаем словарь для хранения данных о заболеваемости в регионах
    regions = {}

    # Читаем данные из файла построчно
    for line in f:
        # Разделяем строку на регион и количество случаев, используя символ табуляции (\t) в качестве разделителя
        region, cases = line.strip().split('  ')

        # Преобразуем количество случаев в целое число
        cases = int(cases)

        # Добавляем регион в словарь или увеличиваем количество случаев, если регион уже есть в словаре
        if region in regions:
            regions[region] += cases
        else:
            regions[region] = cases

    # Определяем максимальное количество случаев
    max_cases = max(regions.values())

    # Отображаем регионы с максимальным количеством случаев
    print('Список регионов с наибольшим количеством случаев:')
    for region, cases in regions.items():
        if cases == max_cases:
            print(region)
